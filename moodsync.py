from sklearn.metrics.pairwise import cosine_similarity
import numpy as np
import pandas as pd

# Mood centroids — average feature values for each mood
mood_centroids = {}
for mood in ['Happy', 'Sad', 'Energetic', 'Calm']:
    mood_df = df[df['mood'] == mood][features]
    mood_centroids[mood] = scaler.transform([mood_df.mean().values])[0]

# Get model confidence score for each song for each mood
X_all = scaler.transform(df[features].values)
all_probs = lr.predict_proba(X_all)  # shape: (n_songs, 4)
classes_list = list(le.classes_)

def smart_playlist(target_mood, n=10, lyric_keywords=None):
    """
    Ranks songs using 3 signals:
    1. Model confidence: how strongly does LR predict this mood?
    2. Feature similarity: how close is the song to the mood's centroid?
    3. Keyword boost: does the track name/artist contain lyric mood words?
    """
    mood_idx = classes_list.index(target_mood)
    centroid = mood_centroids[target_mood]

    # Create a local copy of df and add a temporary positional index column
    local_df = df.copy()
    local_df['__temp_pos_idx__'] = range(len(local_df))

    scores = []
    for _, row in local_df.iterrows():
        pos_idx = row['__temp_pos_idx__'] # Use the guaranteed positional index

        # Signal 1: model confidence (0 to 1)
        confidence = all_probs[pos_idx][mood_idx]

        # Signal 2: cosine similarity to mood centroid (0 to 1)
        song_vec = X_all[pos_idx].reshape(1, -1)
        similarity = cosine_similarity(song_vec, centroid.reshape(1, -1))[0][0]
        similarity = max(0, similarity)

        # Signal 3: lyric keyword boost
        keyword_boost = 0
        if lyric_keywords:
            track = str(row.get('track_name', '')).lower()
            artist = str(row.get('artists', '')).lower()
            combined = track + ' ' + artist
            matches = sum(1 for kw in lyric_keywords if kw.lower() in combined)
            keyword_boost = min(0.3, matches * 0.1)  # max +0.3 boost

        # Weighted final score
        final_score = (confidence * 0.5) + (similarity * 0.35) + (keyword_boost * 0.15)
        scores.append(final_score)

    local_df['_score'] = scores
    result = local_df[local_df['mood'] == target_mood].copy()
    result = result.drop_duplicates(subset='track_name')
    result = result.sort_values('_score', ascending=False).head(n)

    # When mapping 'confidence' to result, use the temporary positional index from 'result'
    result['confidence'] = result['__temp_pos_idx__'].map(
        lambda pos_idx: f"{all_probs[pos_idx][mood_idx]:.0%}"
    )
    result['match_score'] = result['_score'].map(lambda s: f"{s:.2f}")

    # Drop the temporary positional index column from the local copy of df
    local_df.drop(columns=['__temp_pos_idx__'], inplace=True)

    # The global df will not have the '_score' column added here, as it's on local_df
    # So, df.drop(columns=['_score'], inplace=True) in the calling cell is no longer necessary
    # but won't cause an error if it attempts to drop a non-existent column.

    return result[['track_name', 'artists', 'confidence', 'match_score']].reset_index(drop=True)

